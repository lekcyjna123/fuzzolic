int switchf(int v) {
    switch (v) {
        case 0:
            return 1111;
        case 1:
            return 2111;
        case 2:
            return 3333;
        case 3:
            return 4444;
        case 4:
            return 5555;
        case 5:
            return 6666;
        case 6:
            return 7777;
        default:
            return 0;
    }
}